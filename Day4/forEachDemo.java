package Day4;

public class forEachDemo {
    public static void main(String[] args) {
        int[] arr = {1,2,3,4,5};
        for(int i : arr){
            System.out.println(i);
        }

        char[] arr1 = {'a','b','c','d','e'};
        for(char i: arr1){
            System.out.println(i);
        }
    }
}
